           <h1>PATAMBAN, MICHOACÁN</h1>
           Para comprar artesanía, los pueblos michoacanos se pintan solos. Una opción espléndida, sin ser de las más conocidas, 
           es Patamban, en la Meseta Purépecha. Su especialidad es el barro en distintas versiones: natural (con decorado de petatillo), 
           vidriado y bruñido. Muy recomendable resulta la visita a la casa del
           multipremiado maestro artesano Neftalí Ayungua, “Tata Talli”, en Madero No. 12 (a tres cuadras al poniente de la Plaza de Armas).
           <br> <br>
               <b> Cómo llegar:</b> <br>
                   Patamban se localiza a 33 km al sur de Zamora, por la carretera México-Nogales (federal 15) y el ramal a Ocumicho.
