           <h1>MATA ORTIZ, CHIHUAHUA</h1>
           Mata Ortíz es, sin lugar a dudas, uno de los pueblos artesanales más interesantes del país. 
           Desde hace décadas, todas las familias del lugar se dedican a elaborar una cerámica inspirada en las antiguas ollas de la cultura Paquimé 
           (la zona arqueológica está a pocos kilómetros de aquí)
           y lo que han logrado es uno de los productos artesanales más refinados, originales y mejor cotizados de México (lleva suficiente dinero, porque cuesta lo que vale).
           <br> <br>
               <b> Cómo llegar:</b> <br>
           Mata Ortiz se localiza a 27 km al sur de Nuevo Casas Grandes, por la carretera estatal pavimentada.
