           <h1>ZINACANTÁN, CHIAPAS</h1>
           La artesanía chiapaneca puede verse y comprarse en San Cristóbal de las Casas, pero para mirar de cerca los hermosos chales de tonos lila, 
           verde y azul de las mujeres zinacantecas y conocer su delicada manufactura es preciso ir a este pueblo de Los Altos. En la plaza siempre 
           hay muchachas que llevan a los viajeros a sus casas para mostrarles los telares. Ofrecen comida y posh, y a cambio esperan que el viajero haga compras. 
           La experiencia vale la pena.
           <br> <br>
               <b> Cómo llegar:</b> <br>
                   Zinacantán se localiza a 11 km al noroeste de San Cristóbal de las Casas por camino pavimentado a San Juan Chamula.
